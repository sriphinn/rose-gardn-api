DROP TYPE IF EXISTS color_name;
CREATE TYPE color_name AS ENUM (
    'Apricot',
    'Orange',
    'Multi-Color',
    'Pink',
    'Purple',
    'Red',
    'White',
    'Yellow'
)

CREATE TABLE rose_gardn_roses (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id INTEGER
        REFERENCES rose_gardn_users(id) ON DELETE CASCADE NOT NULL,
    photo TEXT,
    name TEXT NOT NULL,
    type type_name,
    color color_name,
    date TIMESTAMPTZ DEFAULT now() NOT NULL
);